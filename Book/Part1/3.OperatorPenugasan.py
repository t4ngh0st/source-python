#   Operator        Simbol
# ============================
#  Pengisian          =
#  Penjumlahan       +=
#  Pengurangan       -=
#  Perkalian         *=
#  Pembagian         /=
#  Sisa Bagi         %=
#  Pemangkatan       **=

# Ambil Input Untuk Mengisi Nilai
a = int(input("Inputkan Nilai a : "))

# Jumlah Nilai a Dengan Operator Penugasan,Yaitu Penugasan
print("Nilai a = " + str(a))

# Jumlah Nilai a Ditambah a Dengan Operator Penugasan,Yaitu Penugasan
a += 5
print("Nilai Setelah Ditambah 5 Dengan Operator +=")
print("Nilai a += 5 = " + str(a))