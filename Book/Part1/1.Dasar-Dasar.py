# Membuat Variable
nama = "Ifdil Jomok" # Variable Dengan Nilai String
umur = 15 # Variable Dengan Nilai Integer
tinggi = 175.5 # Variable Dengan Nilai Float

# Output
print(f"Nama Dia Adalah: {nama},umurnya: {umur} dan tingginya {tinggi}\n")

# Merubah Isi Variable
print(nama) # >> Ifdil Jomok
print(nama.lower()) # >> ifdil jomok
print(nama.upper()) # >> IFDIL JOMOK
print(nama.capitalize()) # >> Ifdil jomok
print(nama.title()) # >> Ifdil Jomok
print(nama.swapcase()) # >> iFDIL jOMOK