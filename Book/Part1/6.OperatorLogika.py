#     Nama        Simbol Di Python
# =================================
#  Logika AND           and
#  Logika OR            or
#  Negasi/Kebalikan     not

a = True
b = False

print("a = " + str(a))
print("b = " + str(b))

# Logika AND
c = a and b
print("Logika AND a dan b ? " + str(c))

# Logika OR
c = a or b
print("Logika OR a dan b ? " + str(c))

# Logika Negasi/Kebalikan a
c = not a
print("Logika Negasi/Kebalikan a ? " + str(c))

# Logika Negasi/Kebalikan b
c = not b
print("Logika Negasi/Kebalikan b ? " + str(c))
