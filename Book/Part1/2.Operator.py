#            == Tabel ==
# Operator      Makna       Contoh      Hasil
# ==========================================================
#    +       Penjumlahan     2 + 3        5
#    -       Pengurangan     3 - 1        2
#    *       Perkalian       3 * 2        6
#    /       Pembagian       4 / 2        2
#   //       Pembagian Tapi Dibulatkan      6 // 5      1
#    %       Modulus(sisa)   5 % 2        1
#   **       Pangkat         3 ** 2       9


# Contoh Script Python

# Mengambil Input Dari User

a = int(input("Masukkan Nilai a >> "))
b = int(input("Masukkan Nilai b >> "))

# Operator Penjumlahan
print("a + b = ", a + b)
# Operator Pengurangan
print("a - b = ", a - b)
# Operator Perkalian
print("a * b = ", a * b)
# Operator Pembagian
print("a / b = ", a / b)
# Operator Pembagian Tapi Dibulatkan
print("a // b = ", a // b)
# Operator Modulus(sisa)
print("a % b = ", a % b)
# Operator Pangkat
print("a ** b = ", a ** b)