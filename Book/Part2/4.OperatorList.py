# Operasi List
# Pengabunggan (+)
# Perkalian(*)

# Operator Pengabungan(+)
# Beberapa List Lagu
list_lagu = [
    'No Women', 'no Cry',
    'Dear God'
]

# Playlist Lagu Favorit
playlist_favorit = [
    'Break Out',
    'Now Loading!!!'
]

# Mengabungkan Kedua Variable List
semua_list = list_lagu + playlist_favorit

# Menampilkan Variable semua_list
print(semua_list)
print(" ")

# operator Perkalian
# Playlist Lagu Favorit
star_pin = [
    'Break Out',
    'Now Loading!!!'
]

# Variable Perkalian
kali = 5

# Menampilkan Variable semua_list
print(star_pin * kali)
print(" ")